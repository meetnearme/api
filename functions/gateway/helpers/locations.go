type City struct {
	ID                   string  `json:"id"`
	City                 string  `json:"city"`
	Latitude             float64 `json:"latitude"`
	Longitude            float64 `json:"longitude"`
	Population           int     `json:"population"`
	State                string  `json:"state"`
	Label                string  `json:"label"`
}

// SOURCE: 1,000 largest US cities https://gist.github.com/Miserlou/c5cd8364bf9b2420bb29
const CitiesJson = `[]`

// ParseCities converts the citiesJson string into a slice of City structs and prints them
// in this case to stdout to copy paste as the struct below
func ParseCities(citiesJson string) {
	var rawCities []map[string]interface{}
	err := json.Unmarshal([]byte(citiesJson), &rawCities)
	if err != nil {
		fmt.Println("Error parsing JSON:", err)
		return
	}

	log.Println("var Cities = []City{")
	for _, rawCity := range rawCities {
		city := City{
			City:                 rawCity["city"].(string),
			Latitude:             rawCity["latitude"].(float64),
			Longitude:            rawCity["longitude"].(float64),
			State:                rawCity["state"].(string),
		}

		population, _ := strconv.Atoi(rawCity["population"].(string))
		city.Population = population

		log.Printf("\tCity{\n")
		log.Printf("\t\tCity:                 %q,\n", city.City)
		log.Printf("\t\tLatitude:             %f,\n", city.Latitude)
		log.Printf("\t\tLongitude:            %f,\n", city.Longitude)
		log.Printf("\t\tPopulation:           %d,\n", city.Population)
		log.Printf("\t\tState:                %q,\n", city.State)
		log.Printf("\t},\n")
	}

	log.Println("}")

}

var CityIndex map[string][]int

func init() {
	CityIndex = make(map[string][]int)
	for i, city := range Cities {
			lowerCity := strings.ToLower(city.City)
			lowerState := strings.ToLower(city.State)
			CityIndex[lowerCity] = append(CityIndex[lowerCity], i)
			CityIndex[lowerState] = append(CityIndex[lowerState], i)
	}
}


func SearchCitiesIndexed(query string) []City {
	query = strings.ToLower(query)
	var matches []City
	seen := make(map[int]bool)

	for key, indices := range CityIndex {
		if strings.Contains(key, query) {
			for _, index := range indices {
				if !seen[index] {
					city := Cities[index]
					city.ID = uuid.NewString()
					// Create a new label combining city and state
					city.Label = fmt.Sprintf("%s, %s", city.City, city.State)
					// Remove city, state, and population fields
					city.City = ""
					city.State = ""
					city.Population = 0
					matches = append(matches, city)
					seen[index] = true
				}
			}
		}
	}

	return matches
}

